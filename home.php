<h2>
MMV : Hostel Foundation
</h2>
<p>To establish the Women's College, a plot within the university covering 45 acres of land was selected. 
Mahamana's vision was soon translated into reality when Makanji Khatau of Bombay and his relatives donated a sum of Rs. 2.8 lac. 
This amount was used to construct <u>"Swasti Kunj"</u>, the first building of Mahila Mahavidyalaya. 
It served as the hostel as well as the college building. </p>
<p>Although the building was completed by the end of 1924, the endowment money was not enough to start the college. 
Therefore, the college came into existence only in the session 1928-29. 
Thirty three students enrolled as boarders in the first year. 
The college started with three lecturers and Ms A. Aryanayakam n√©e Adhikari, appointed Lecturer in Sanskrit served as the first Principal. 
Later several eminent scholars and dedicated educationists served on the faculty. 
These include Ms Jamuna Devi Pathak, Ms Leela Desai, Ms V.S. Deodhar, Ms Shanti Kumari Bajpayee, 
Dr Gyanwati Trivedi, Prof Padma Mishra and Prof Kamalveni Chandrashekhar.</p>
<p>Subsequently, the main building of the college came up. Initially, the Central Hall surrounded by a few rooms provided the infrastructure. 
Gradually the strength of the college went up and more disciplines were introduced. 
In 1948, Dr S. Radhakrishnan, the then Vice-Chancellor, introduced Science subjects. Since then there has been no looking back. 
Presently, sanctioned faculty strength is 119, and the number of students about 1200.</p>
